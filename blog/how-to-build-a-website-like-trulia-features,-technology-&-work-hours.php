<h3>Pro-tips:</h3>
<ul>
	<li>
		<p>In the US, Multiple Listing Services (MLSs) are technologically supported by the National Association of REALTORS® (NAR). This body ensures the interoperability of data between MLS systems. As of 2020, there were 597+ MLSs in the US. Brokers that participate in an MLS agree to share their listings with other MLS participants.</p>
	</li>
		<li>
		<p>MLSs allow real estate portals like Trulia to publish MLS listings on their websites. This data is shared with participating brokers or software technology partners through their own web APIs. Trulia gets access to local MLSs by negotiating data sharing and syndication agreements. </p>
	</li>
		<li>
		<p>In the US, public property details should comply with rules established by the NAR and must adhere to the IDX (Internet Data Exchange) policy. To enable data exchange between an MLS database and the real estate website, you need to integrate IDX. </p>
	</li>
		<li>
		<p>IDX is a set of standards, rules, and technologies that track real estate data as it moves from an MLS to your website. IDX moderates how your website accesses MLS data and how it's publicly displayed.
</p>
	</li>
		<li>
		<p>Depending on the MLS that your website pulls the data from, your platform can get listings through different methods, including iFrame, FTP, and RETS. Recently, the most popular method is the RESO Web API. However, there are third-party services that also provide APIs for this task. For instance, SimplyRets, ATTOM Property API, and Mashvisor API. 
</p>
	</li>
</ul>

