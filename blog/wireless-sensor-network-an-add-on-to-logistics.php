						<h1>Wireless Sensor Network: An Add-on to Logistics</h1>
						<p>The transport and delivery of goods from one continent to other and between different locations on land has undoubtedly empowered the business growth but simultaneously has caused mismanagement and various accidents. The unusual accidents and mishappenings have caused a significant impact on individual businesses, the economy of a country, workers and the surrounding environment. Every year in each of the seven seas, more than 100 million shipping containers are circulated around the globe and transport of around 90% of the worldâ€™s non-bulk cargo happens. These containers with the help of intermodal transport get connected by road, rail and air freight networks to deliver the goods across all points of the globe.</p>
						<p>With containers losing at sea was serving as a major challenge, other challenges mostly destroyed the value of goods being transported, caused damage to nearby communities and areas and put a negative impact on the economy. And at the times when water got introduced into the cargo, it used to destroy the value of the goods and somewhere altered the temperature of the containers. Storing containers next to each other in an incorrect way and close to dangerous goods increased the risk in the accident. On the same side, land movement of containers when transported along unsafe roads and routes resulted in spills and destroyed the environment which caused damage to urban or rural areas.</p>
						<p>Therefore, many companies have started using M2M (Machine-to-Machine) Solutions which has GPS and GPRS/3G technologies incorporated into the machines which have reduced these risks and strengthened the economic value of the freight transportation on a global level.</p>
						<h2>HOW WIRELESS SENSOR NETWORK (WSN) IS CONTRIBUTING TO FREIGHT MONITORING?</h2>
						<p>The logistics companies have got a unique and out of the box solution with the help of Machine-to-Machine communication. Not only it has enabled the tracking of the package from source to destination but monitoring the cargo in real time throughout the journey has also become a reality.</p>
						<h2>ADVANTAGES OF USING WIRELESS SENSOR TECHNOLOGY IN LOGISTICS OPERATIONS:</h2>
						<ul>
						<li><b>TRACKING THE GOODS IN REAL-TIME:</b><br> The presence of Sensors has allowed the transportation companies, cargo owners and end customers to track that where the goods are located at all stages of the transportation process. Although tracking the real-time information in the case of sensitive goods like stones, metals and dangerous goods is very critical. GPS Sensors help in monitoring the location at all times whereas GPRS/3G helps in sending the alerts. For the location where GPRS/3G coverage is not possible because of remote areas, the SD Card contains the programmed route that one has to follow and can compare it with the distance traveled. Later on, you can send alerts if the driver diverges from the route that has been set. The SD card plays an important role when the GPRS/3G coverage is in remote areas as it can store coordinates of the route being taken by the driver.</li>
						<li><b>DETECTION OF UNEXPECTED CONTAINER OPENINGS:</b><br> A sensor is placed inside the container which tells that when a container has been opened. The sensors are programmed in a way that they tell for how much time a container was kept opened which corresponds to the scheduled inspections and alerts. All the devices are incorporated with a real-time clock which records the date and time of all events that occur.</li>
						<li><b>TRACKING TRANSPORTATION CONDITIONS:</b><br> In some of the cases, the containers contain humidity and a temperature difference, the sensors measure the environmental variables and detect whether the goods are fragile or not and can be managed or not. The 3-axis accelerometer detects the vibrations and hence alerts are sent accordingly. The combination of sensors and GPS tracks each and every event and takes action accordingly.</li>
						<li><b>IDENTIFICATION OF STORAGE INCOMPATIBILITIES:</b> <br>With the help of a network between RFID and other sensors, the exchange of information was no more a challenge. In a similar way, warning messages are sent such that in any case goods are placed side by side with flammable materials than they are separated apart after those warnings. Not only this but a strict list of safety regulations should be followed when reactive substances are stored in a container.
						<p>Following are the situations to be avoided:</p>
						<ul>
						<li>Storage of incompatible substances in close proximity of each other.</li>
						<li>Exceeding the maximum storage volume threshold for a hazardous substance.</li>
						<li>To store hazardous substances in unprotected areas for the longer duration of time than a specific maximum time period.</li>
						</ul>
						</li>
						</ul>
						<p>Henceforth, the integration of ubiquitous technologies and the inexpensive sensors has helped in reducing the execution costs and has improved the response time in safety-critical situations. Therefore, the communication between the process experts and the real-time devices has improved the overall responsiveness.</p>
