<h2>#5 Additional Factors
</h2>
<p>To get a competitive edge in the market, consider adding more useful features to enrich your customers' shopping journey. 
</p>
<p>Model of Development and Timeline
</p>
<p>Decide whether your team will follow the iterative development approach (waterfall model) or agile development approach. This will directly impact the app's robustness and Time To Market (TTL).
</p>
<p>MVP Or Full Fledged Development
</p>
<p>A minimum viable product (MVP) is developed with essential features only. Then, based on customer feedback, more functionalities are added to the app. Sometimes, unnecessary features are also dropped off. A full-fledged development means that all the features of the app will be developed in one go. This can be risky and impact the business profitability if the app does not perform well in the market.
</p>
