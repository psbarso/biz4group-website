<h2>
Proposed Business Model of Medicine Delivery Apps
</h2>

<p>Your application will serve as a bridge between doctors, patients and pharmacies. Think of it as a two-step process, where the first step is to consult a doctor and the second to order the prescribed medicines from the app. This is exactly the model followed in the 'Practo App".
</p>
<p>Here is how a typical medical/ pharma delivery app should function:
</p>

<ul>
	<li>
		<p>Patient books an appointment with a doctor of required specialization</p>
	</li>
	<li>
		<p>The doctor prescribes some medicines based on diagnosis</p>
	</li>
	<li>
		<p>The patient uses the ePharmacy dashboard on the same application to place an order for the medicines, upload the prescription document and makes the payment</p>
	</li>
	<li>
		<p>After verifying the prescription document, the payment is accepted, and the order is placed</p>
	</li>
	<li>
		<p>The ePharmacy dashboard is stocked with medicines from pharmaceutical companies and distributors
</p>
	</li>
	<li>
		<p>Once an order is approved, the delivery partner will deliver the medicine to the patient's location</p>
	</li>
</ul>

