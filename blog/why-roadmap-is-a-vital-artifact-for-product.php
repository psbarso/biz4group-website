						<h1>Why roadmap is a vital artifact for Product?</h1>
						<p>The product development roadmap is a vital document for tracking demand and delivery. Product managers are responsible for defining and managing the roadmap for commercial products delivered by software product companies. This role does not typically exist in enterprise development groups. In those kinds of organizations, product management tasks will fall to a project manager who must adopt the tasks of product or portfolio manager. The main responsibility of this role is creating and maintaining the roadmap as it is the organizationâ€™s tool to communicate product and portfolio planning.</p>
						<p>Once a roadmap is in place, it can be used to gain internal alignment by socializing it with key stakeholders and development teams. This will result in improved communication to both internal and external audiences as a product vision is outlined. Besides the benefit of showing progress with product development, the roadmap helps customers and partners integrate the product plans into their own long-term strategies.</p>
						<p>A good roadmap will contain at least the following information:</p>
						<ol>
						<li>Summary of strategic objectives</li>
						<li>Evaluation criteria, their definitions, and their weightings</li>
						<li>List of potential projects, their sponsors and where the requirements can be found</li>
						<li>Evaluation criteria results for each potential project.</li>
						<li>Prioritized list of products that have met the criteria.</li>
						<li>Delivery map showing planned product deliveries with approximate dates</li>
						</ol>
