					<h2>Steps to build an eLearning App and Website like Udemy/Unacademy</h2>

						<h3><strong>#1 Find your Model</strong></h3>
<img style="padding-bottom: 10px" src="<?php echo SITE_URL;?>/images/Find-your-Model.jpg" alt="Find-your-Model">

						<p>A typical e-learning platform can provide the following services: </p>
					<ul>
						<li>
							<p>Massive Online Open Courses (MOOCs) </p>
						</li>
							<li>
							<p>Virtual learning environment (VLE)</p>
						</li>
						<li>
							<p>Video streaming services</p>
						</li>
						<li>
							<p>Instructor-led training (ILT) </p>
						</li>
						<li>
							<p>Podcasts</p>
						</li>
					</ul>
					<p>Once you've finalized the kind of service your website will cater to, the next step is to decide how to monetize the service. Here are some models for you to choose from:
					</p>

					<ul>
						<li>
							<p><strong>Paid Certificates:</strong> Create tie-ups with universities and educational organizations to provide learners with verified paid certificates</p>
						</li>
							<li>
							<p><strong>Subscription-Based Model:</strong> The learner can buy paid subscriptions to the website on a monthly or yearly basis. This will provide them with unlimited access to the website and all its resources.
 
</p>
						</li>
							<li>
							<p><strong> Corporate Model:</strong> This model is specific to organizations who want to train their employees in a certain skill set and purchase a subscription for each of them from your website. </p>
						</li>
							<li>
							<p><strong>Paid Courses:</strong>  In this model, part of the capital invested by the student will be sent to the instructor who uploaded the course, and the rest will go to the website owner.</p>
						</li>
					</ul>

