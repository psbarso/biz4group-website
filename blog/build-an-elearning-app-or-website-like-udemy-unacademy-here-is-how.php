					<h3><strong>#3 Finalize Features of the Website/App</strong></h3>

					<img style="padding-bottom: 10px" src="<?php echo SITE_URL;?>/images/Finalize-Features-of-the-Website-App.jpg" alt="Finalize-Features-of-the-Website-App">

					<p>A website like Udemy/Unacademy includes features like:
</p>
	
					<ul>
						<li>
							<p><strong>Easy Register and Sign-up</strong></p>
						</li>
							<li>
							<p><strong>Student's Profile: </strong>	Students can update their profiles with details like age, university, subject of interest, etc. The profile can be edited whenever necessary. 
</p>
						</li>
							<li>
							<p><strong>Teacher's Profile:</strong> Educators can enlist their areas of expertise and other essential details that can later be edited/ updated whenever needed</p>
						</li>
							<li>
							<p><strong>Filters:</strong>	 Students can enroll in classes based on different criteria like subject, topic, language, ratings, price, duration, level of difficulty, etc. 
</p>
						</li>
							<li>
							<p><strong>Recommendations:</strong>	 The app/ platform must have a section to show trending courses, certificates, top-rated tutors, and related courses for the students to select from. For instance, if a learner has enrolled in a beginner course to AI, the website can suggest an intermediate level AI course as a follow-up.   
</p>
						</li>
							<li>
							<p><strong>Feedback and rating system:</strong>	 Students can rate a course and leave feedback in the comment section of the course.  </p>
						</li>
							<li>
							<p><strong>Dashboard: </strong>It is a centralized panel with quick links, reports and actions related to a particular profile. It is very different for instructors and learners.
</p>
						</li>
					</ul>

					<p><strong>i.	For instructors:</strong> The dashboard provides them with analytical data to view the number of enrolled students, ratings, and conversions. It can also allow instructors to create a course, upload study material, resources, etc. There should also be a forum for communication with students. 

					</p>
					<p><strong>ii.	For students:</strong> They can see the progress made in the current course, certificates obtained, learning progress/ goals achieved and ratings given along with the purchase history. Students can also save their favorite courses into the "Wishlist" for later enrollment.
</p>
					<ul>
						<li><p><strong>Payment Gateways: </strong>The platform/ app must be integrated with secure and trusted payment gateways to ensure maximum safety during fund transfer</p></li>
							<li><p><strong>Admin Panel: </strong>The business/ app owner can manage both instructors and students from a centralized dashboard. He can also gather statistical reports, add new content to the website, and assign user roles and privileges. </p></li>
								<li><p><strong>Additional Features:</strong>	 Support section for website-related help along with a blog and newsletter section for sharing articles on trending topics.
</p></li>
					</ul>

					<h3><strong>#4 Development Approach</strong></h3>

						<img style="padding-bottom: 10px" src="<?php echo SITE_URL;?>/images/Development-Approach.jpg" alt="Development-Approach">

					<p>There are three factors to consider while finalizing the development approach for an eLearning platform/ app.
					</p>

					<p><strong>Model of Development:</strong></p>
					<p>This can either be a Waterfall approach or an Agile development approach. 
In the waterfall model, features are developed iteratively. After every iteration, client feedback is taken into consideration, and the features are improved. However, in Agile development, features are developed parallelly. Again, there are iterations involved, and after every iteration, the client feedback is implemented.
The selection of the development mode considerably impacts the cost, timeline and effectiveness of the project.
</p>

<p><strong>Mode of Development:
</strong></p>
<p>Custom software development is more expensive and time-consuming. However, it allows you to create a website/app with any feature that you would like. In contrast, a template-based model comes with many free plugins and basic functionalities already hardcoded. However, you may face compatibility issues while adding a specific functionality other than what is already available. 
</p>

<p><strong>Tech Stack for Development:</strong>
</p>
<p>Here are some technologies for you to choose from:</p>
<p>Back-end- PHP, JavaScript, Python, Java
</p>
<p>Database- MySQL, MongoDB
</p>
<p>Front-end- JavaScript, AngularJS, HTML5, CSS3, React
</p>
<p>Popular Payment Gateways: Stripe, PayPal.</p>

<p><strong>UI/UX Development:</strong></p>
<p>This is a very important aspect to consider while developing an eLearning website or app. 
</p>
<p>The UX/UI design is an iterative process that ensures navigating your website is a seamless task. As a result, websites like Udemy are easy to navigate and are simple and consistent in design. This makes it efficient to use for students and instructors (who are mostly not so tech-savvy).
</p>
<p>There are certain considerations to take into account while defining the UI for your eLearning website:
</p>
<ul>
	<li>
		<p>Colors and illustrations should be carefully chosen while performing accessibility checks for differently-abled people.
</p>
	</li>
		<li>
		<p>Typography should be readable and visually appealing without being too busy. Business keywords can be tagged here so that search engine algorithms deliver favorable results every time. 

</p>
	</li>
</ul>
<p>A simple and clean UI/UX design will also reduce the loading and navigation time of pages/ screens. Opt for a minimalistic style to appeal to a wider audience base. 
</p>

